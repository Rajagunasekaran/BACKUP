DROP TRIGGER IF EXISTS TRG_ACCESS_CONFIGURATION_UPDATE;
CREATE TRIGGER TRG_ACCESS_CONFIGURATION_UPDATE  
AFTER UPDATE ON ACCESS_CONFIGURATION
FOR EACH ROW
BEGIN 
-- declaration for old_value and new_value to store it in TICKLER_HISTORY table
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
-- get the OLD VALUE & NEW_VALUE for ACN_ID to store it in TICKLER_HISTORY table
IF ((OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.ACN_DATA!= NEW.ACN_DATA) OR (OLD.ACN_INITIALIZE_FLAG IS NULL AND NEW.ACN_INITIALIZE_FLAG IS NOT NULL))  THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'ACN_ID=', OLD.ACN_ID,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for CGN_ID to store it in TICKLER_HISTORY table
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for ACN_DATA to store it in TICKLER_HISTORY table
IF (OLD.ACN_DATA!= NEW.ACN_DATA) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ACN_DATA=', OLD.ACN_DATA,','); 
END IF;
IF (OLD.ACN_DATA!= NEW.ACN_DATA) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ACN_DATA=', NEW.ACN_DATA,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for ACN_INITIALIZE_FLAG to store it in TICKLER_HISTORY table
IF (OLD.ACN_INITIALIZE_FLAG IS NULL AND NEW.ACN_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ACN_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ACN_INITIALIZE_FLAG=',NEW.ACN_INITIALIZE_FLAG,',');
ELSEIF (OLD.ACN_INITIALIZE_FLAG IS NOT NULL AND NEW.ACN_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ACN_INITIALIZE_FLAG=',OLD.ACN_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ACN_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.ACN_INITIALIZE_FLAG!= NEW.ACN_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ACN_INITIALIZE_FLAG=', OLD.ACN_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ACN_INITIALIZE_FLAG=', NEW.ACN_INITIALIZE_FLAG,','); 
END IF;
IF ((OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.ACN_DATA!= NEW.ACN_DATA) OR (OLD.ACN_INITIALIZE_FLAG IS NULL AND NEW.ACN_INITIALIZE_FLAG IS NOT NULL))  THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.ACN_TIMESTAMP!= NEW.ACN_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ACN_TIMESTAMP=', OLD.ACN_TIMESTAMP,','); 
END IF;
END IF;
-- inserting old_values and new_values in the TICKLER_HISTORY table with their corresponding TTIP_ID in TICKLER_TABID_PROFILE
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE!=NEW_VALUE)THEN
-- REMOVING COMMA(,) AT THE END OF OLD_VALUE & NEW_VALUE
 SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
  SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM ACCESS_CONFIGURATION WHERE ACN_ID=NEW.ACN_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='ACCESS_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;