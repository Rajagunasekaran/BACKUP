DROP PROCEDURE IF EXISTS SP_TRG_STAFFDLY_STAFF_VALIDATION;
CREATE PROCEDURE SP_TRG_STAFFDLY_STAFF_VALIDATION(
IN ECNID INTEGER,
IN INVOICEDATE DATE,
IN INVOICEAMOUNT INTEGER,
IN PROCESS TEXT)
BEGIN
  DECLARE ERRORMSG TEXT;
	DECLARE MESSAGE_TEXT VARCHAR(50);
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE') THEN
		IF(INVOICEDATE > CURDATE()) THEN
      SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=474);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERRORMSG;
		END IF;
		IF NOT EXISTS(SELECT ECN_ID FROM EXPENSE_CONFIGURATION WHERE ECN_ID = ECNID AND CGN_ID = 26)THEN
      SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=515);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERRORMSG;
		END IF;
    IF (INVOICEAMOUNT IS NOT NULL) THEN
        SET @INVOICE_AMT=(SELECT SUBSTRING_INDEX(INVOICEAMOUNT, '.', 1));
        -- CHECKING THE LENGTH OF THE AMOUNT
        IF(LENGTH(@INVOICE_AMT)>3) THEN
  				SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=567);
          SET ERRORMSG=(SELECT REPLACE (ERRORMSG,'[DIGIT]','3'));
          SIGNAL SQLSTATE '45000'
  				SET MESSAGE_TEXT = ERRORMSG;
        END IF;
			END IF;
	END IF;
END;