  PREPARE DROP_TEMP_INSERT_MENU_STMT FROM @DROP_TEMP_INSERT_MENU;
  EXECUTE DROP_TEMP_INSERT_MENU_STMT;
  
  SET @DROP_TEMP_REMOVE_MENU=(SELECT CONCAT('DROP TABLE ',TEMP_REMOVE_MENU));
  PREPARE DROP_TEMP_REMOVE_MENU_STMT FROM @DROP_TEMP_REMOVE_MENU;
  EXECUTE DROP_TEMP_REMOVE_MENU_STMT;
 
  SET @DROP_TEMP_INSERT_FILE=(SELECT CONCAT('DROP TABLE ',TEMP_INSERT_FILE));
  PREPARE DROP_TEMP_INSERT_FILE_STMT FROM @DROP_TEMP_INSERT_FILE;
  EXECUTE DROP_TEMP_INSERT_FILE_STMT;
  
  SET @DROP_TEMP_REMOVE_FILE=(SELECT CONCAT('DROP TABLE ',TEMP_REMOVE_FILE));
  PREPARE DROP_TEMP_REMOVE_FILE_STMT FROM @DROP_TEMP_REMOVE_FILE;
