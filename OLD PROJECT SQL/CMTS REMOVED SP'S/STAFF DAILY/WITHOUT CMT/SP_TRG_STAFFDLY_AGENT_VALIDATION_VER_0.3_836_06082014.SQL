DROP PROCEDURE IF EXISTS SP_TRG_STAFFDLY_AGENT_VALIDATION;
CREATE PROCEDURE SP_TRG_STAFFDLY_AGENT_VALIDATION(
IN AGENTDATE DATE,
IN COMMAMOUNT INTEGER,
IN PROCESS TEXT)
BEGIN
	DECLARE ERRORMSG TEXT;
	DECLARE MESSAGE_TEXT VARCHAR(50);
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE') THEN
		IF(AGENTDATE > CURDATE()) THEN
			SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=516);      
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERRORMSG;
		END IF;
		IF (COMMAMOUNT IS NOT NULL) THEN
			SET @COM_AMT=(SELECT SUBSTRING_INDEX(COMMAMOUNT, '.', 1));
			IF(LENGTH(@COM_AMT)>4) THEN
				SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=567);
				SET ERRORMSG=(SELECT REPLACE (ERRORMSG,'[DIGIT]','4'));
				SIGNAL SQLSTATE '45000'
				SET MESSAGE_TEXT = ERRORMSG;
			END IF;
		END IF;
	END IF;
END;