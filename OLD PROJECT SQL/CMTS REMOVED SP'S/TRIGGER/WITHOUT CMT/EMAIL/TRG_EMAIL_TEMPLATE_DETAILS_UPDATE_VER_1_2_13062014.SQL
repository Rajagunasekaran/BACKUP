DROP TRIGGER IF EXISTS TRG_EMAIL_TEMPLATE_DETAILS_UPDATE;
CREATE TRIGGER TRG_EMAIL_TEMPLATE_DETAILS_UPDATE  
AFTER UPDATE ON EMAIL_TEMPLATE_DETAILS 
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF ((OLD.ET_ID!= NEW.ET_ID) OR (OLD.ETD_EMAIL_SUBJECT!= NEW.ETD_EMAIL_SUBJECT) OR (OLD.ETD_EMAIL_BODY!= NEW.ETD_EMAIL_BODY)) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'ETD_ID=', OLD.ETD_ID,','); 
END IF;
IF (OLD.ET_ID!= NEW.ET_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ET_ID=', OLD.ET_ID,','); 
END IF;
IF (OLD.ET_ID!= NEW.ET_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ET_ID=', NEW.ET_ID,','); 
END IF;
IF (OLD.ETD_EMAIL_SUBJECT!= NEW.ETD_EMAIL_SUBJECT) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ETD_EMAIL_SUBJECT=', OLD.ETD_EMAIL_SUBJECT,','); 
END IF;
IF (OLD.ETD_EMAIL_SUBJECT!= NEW.ETD_EMAIL_SUBJECT) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ETD_EMAIL_SUBJECT=', NEW.ETD_EMAIL_SUBJECT,','); 
END IF;
IF (OLD.ETD_EMAIL_BODY!= NEW.ETD_EMAIL_BODY) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ETD_EMAIL_BODY=', OLD.ETD_EMAIL_BODY,','); 
END IF;
IF (OLD.ETD_EMAIL_BODY!= NEW.ETD_EMAIL_BODY) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ETD_EMAIL_BODY=', NEW.ETD_EMAIL_BODY,','); 
END IF;
IF ((OLD.ET_ID!= NEW.ET_ID) OR (OLD.ETD_EMAIL_SUBJECT!= NEW.ETD_EMAIL_SUBJECT) OR (OLD.ETD_EMAIL_BODY!= NEW.ETD_EMAIL_BODY)) THEN 
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.ETD_TIMESTAMP!= NEW.ETD_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ETD_TIMESTAMP=', OLD.ETD_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EMAIL_TEMPLATE_DETAILS  WHERE ETD_ID=NEW.ETD_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EMAIL_TEMPLATE_DETAILS'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;