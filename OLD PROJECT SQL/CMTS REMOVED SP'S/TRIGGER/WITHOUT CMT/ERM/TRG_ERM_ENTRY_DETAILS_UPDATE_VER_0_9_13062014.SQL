DROP TRIGGER IF EXISTS TRG_ERM_ENTRY_DETAILS_UPDATE;
CREATE TRIGGER TRG_ERM_ENTRY_DETAILS_UPDATE
AFTER UPDATE ON ERM_ENTRY_DETAILS
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF ((OLD.ERM_CUST_NAME != NEW.ERM_CUST_NAME ) OR (OLD.ERM_RENT!= NEW.ERM_RENT) OR (OLD.ERM_MOVING_DATE!= NEW.ERM_MOVING_DATE) 
OR (OLD.ERM_MIN_STAY!= NEW.ERM_MIN_STAY) OR (OLD.ERMO_ID!= NEW.ERMO_ID) OR (OLD.NC_ID IS NULL AND NEW.NC_ID IS NOT NULL) OR (OLD.NC_ID IS NOT NULL AND NEW.NC_ID IS NULL) OR (OLD.NC_ID != NEW.NC_ID)
OR (OLD.ERM_NO_OF_GUESTS IS NULL AND NEW.ERM_NO_OF_GUESTS IS NOT NULL) OR (OLD.ERM_NO_OF_GUESTS IS NOT NULL AND NEW.ERM_NO_OF_GUESTS IS NULL) OR (OLD.ERM_NO_OF_GUESTS != NEW.ERM_NO_OF_GUESTS)
OR (OLD.ERM_AGE IS NULL AND NEW.ERM_AGE IS NOT NULL) OR (OLD.ERM_AGE IS NOT NULL AND NEW.ERM_AGE IS NULL) OR (OLD.ERM_AGE != NEW.ERM_AGE)
OR (OLD.ERM_CONTACT_NO IS NULL AND NEW.ERM_CONTACT_NO IS NOT NULL) OR (OLD.ERM_CONTACT_NO IS NOT NULL AND NEW.ERM_CONTACT_NO IS NULL) OR (OLD.ERM_CONTACT_NO !=NEW.ERM_CONTACT_NO) 
OR (OLD.ERM_EMAIL_ID IS NULL AND NEW.ERM_EMAIL_ID IS NOT NULL) OR (OLD.ERM_EMAIL_ID IS NOT NULL AND NEW.ERM_EMAIL_ID IS NULL) OR (OLD.ERM_EMAIL_ID != NEW.ERM_EMAIL_ID)
OR (OLD.ERM_COMMENTS!= NEW.ERM_COMMENTS)) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'ERM_ID=', OLD.ERM_ID,','); 
END IF;
IF (OLD.ERM_CUST_NAME != NEW.ERM_CUST_NAME ) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_CUST_NAME =', OLD.ERM_CUST_NAME ,','); 
END IF;
IF (OLD.ERM_CUST_NAME != NEW.ERM_CUST_NAME ) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERM_CUST_NAME =', NEW.ERM_CUST_NAME ,','); 
END IF;
IF (OLD.ERM_RENT!= NEW.ERM_RENT) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_RENT=', OLD.ERM_RENT,','); 
END IF;
IF (OLD.ERM_RENT!= NEW.ERM_RENT) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERM_RENT=', NEW.ERM_RENT,','); 
END IF;
IF (OLD.ERM_MOVING_DATE!= NEW.ERM_MOVING_DATE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_MOVING_DATE=', OLD.ERM_MOVING_DATE,','); 
END IF;
IF (OLD.ERM_MOVING_DATE!= NEW.ERM_MOVING_DATE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERM_MOVING_DATE=', NEW.ERM_MOVING_DATE,','); 
END IF;
IF (OLD.ERM_MIN_STAY!= NEW.ERM_MIN_STAY) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_MIN_STAY=', OLD.ERM_MIN_STAY,','); 
END IF;
IF (OLD.ERM_MIN_STAY!= NEW.ERM_MIN_STAY) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERM_MIN_STAY=', NEW.ERM_MIN_STAY,','); 
END IF;
IF (OLD.ERMO_ID!= NEW.ERMO_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERMO_ID=', OLD.ERMO_ID,','); 
END IF;
IF (OLD.ERMO_ID!= NEW.ERMO_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERMO_ID=', NEW.ERMO_ID,','); 
END IF;
IF (OLD.NC_ID IS NULL AND NEW.NC_ID IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'NC_ID=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'NC_ID=',NEW.NC_ID,',');
ELSEIF (OLD.NC_ID IS NOT NULL AND NEW.NC_ID IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'NC_ID=',OLD.NC_ID,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'NC_ID=','<NULL>,');
ELSEIF (OLD.NC_ID!= NEW.NC_ID) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'NC_ID=', OLD.NC_ID,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'NC_ID=', NEW.NC_ID,','); 
END IF;
IF (OLD.ERM_NO_OF_GUESTS IS NULL AND NEW.ERM_NO_OF_GUESTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_NO_OF_GUESTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_NO_OF_GUESTS=',NEW.ERM_NO_OF_GUESTS,',');
ELSEIF (OLD.ERM_NO_OF_GUESTS IS NOT NULL AND NEW.ERM_NO_OF_GUESTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_NO_OF_GUESTS=',OLD.ERM_NO_OF_GUESTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_NO_OF_GUESTS=','<NULL>,');
ELSEIF (OLD.ERM_NO_OF_GUESTS!= NEW.ERM_NO_OF_GUESTS) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'ERM_NO_OF_GUESTS=', OLD.ERM_NO_OF_GUESTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ERM_NO_OF_GUESTS=', NEW.ERM_NO_OF_GUESTS,','); 
END IF;
IF (OLD.ERM_AGE IS NULL AND NEW.ERM_AGE IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_AGE=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_AGE=',NEW.ERM_AGE,',');
ELSEIF (OLD.ERM_AGE IS NOT NULL AND NEW.ERM_AGE IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_AGE=',OLD.ERM_AGE,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_AGE=','<NULL>,');
ELSEIF (OLD.ERM_AGE!= NEW.ERM_AGE) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'ERM_AGE=', OLD.ERM_AGE,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ERM_AGE=', NEW.ERM_AGE,','); 
END IF;
IF (OLD.ERM_CONTACT_NO IS NULL AND NEW.ERM_CONTACT_NO IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_CONTACT_NO=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_CONTACT_NO=',NEW.ERM_CONTACT_NO,',');
ELSEIF (OLD.ERM_CONTACT_NO IS NOT NULL AND NEW.ERM_CONTACT_NO IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_CONTACT_NO=',OLD.ERM_CONTACT_NO,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_CONTACT_NO=','<NULL>,');
ELSEIF (OLD.ERM_CONTACT_NO!= NEW.ERM_CONTACT_NO) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'ERM_CONTACT_NO=', OLD.ERM_CONTACT_NO,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ERM_CONTACT_NO=', NEW.ERM_CONTACT_NO,','); 
END IF;
IF (OLD.ERM_EMAIL_ID IS NULL AND NEW.ERM_EMAIL_ID IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_EMAIL_ID=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_EMAIL_ID=',NEW.ERM_EMAIL_ID,',');
ELSEIF (OLD.ERM_EMAIL_ID IS NOT NULL AND NEW.ERM_EMAIL_ID IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ERM_EMAIL_ID=',OLD.ERM_EMAIL_ID,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ERM_EMAIL_ID=','<NULL>,');
ELSEIF (OLD.ERM_EMAIL_ID!= NEW.ERM_EMAIL_ID) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'ERM_EMAIL_ID=', OLD.ERM_EMAIL_ID,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ERM_EMAIL_ID=', NEW.ERM_EMAIL_ID,','); 
END IF;
IF (OLD.ERM_COMMENTS!= NEW.ERM_COMMENTS) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_COMMENTS=', OLD.ERM_COMMENTS,','); 
END IF;
IF (OLD.ERM_COMMENTS!= NEW.ERM_COMMENTS) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ERM_COMMENTS=', NEW.ERM_COMMENTS,','); 
END IF;
IF ((OLD.ERM_CUST_NAME != NEW.ERM_CUST_NAME ) OR (OLD.ERM_RENT!= NEW.ERM_RENT) OR (OLD.ERM_MOVING_DATE!= NEW.ERM_MOVING_DATE) 
OR (OLD.ERM_MIN_STAY!= NEW.ERM_MIN_STAY) OR (OLD.ERMO_ID!= NEW.ERMO_ID) OR (OLD.NC_ID IS NULL AND NEW.NC_ID IS NOT NULL) OR (OLD.NC_ID IS NOT NULL AND NEW.NC_ID IS NULL) OR (OLD.NC_ID != NEW.NC_ID)
OR (OLD.ERM_NO_OF_GUESTS IS NULL AND NEW.ERM_NO_OF_GUESTS IS NOT NULL) OR (OLD.ERM_NO_OF_GUESTS IS NOT NULL AND NEW.ERM_NO_OF_GUESTS IS NULL) OR (OLD.ERM_NO_OF_GUESTS != NEW.ERM_NO_OF_GUESTS)
OR (OLD.ERM_AGE IS NULL AND NEW.ERM_AGE IS NOT NULL) OR (OLD.ERM_AGE IS NOT NULL AND NEW.ERM_AGE IS NULL) OR (OLD.ERM_AGE != NEW.ERM_AGE)
OR (OLD.ERM_CONTACT_NO IS NULL AND NEW.ERM_CONTACT_NO IS NOT NULL) OR (OLD.ERM_CONTACT_NO IS NOT NULL AND NEW.ERM_CONTACT_NO IS NULL) OR (OLD.ERM_CONTACT_NO !=NEW.ERM_CONTACT_NO) 
OR (OLD.ERM_EMAIL_ID IS NULL AND NEW.ERM_EMAIL_ID IS NOT NULL) OR (OLD.ERM_EMAIL_ID IS NOT NULL AND NEW.ERM_EMAIL_ID IS NULL) OR (OLD.ERM_EMAIL_ID != NEW.ERM_EMAIL_ID)
OR (OLD.ERM_COMMENTS!= NEW.ERM_COMMENTS)) THEN 
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.ERM_TIMESTAMP!= NEW.ERM_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ERM_TIMESTAMP=', OLD.ERM_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE!=NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM ERM_ENTRY_DETAILS  WHERE ERM_ID=NEW.ERM_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='ERM_ENTRY_DETAILS'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;